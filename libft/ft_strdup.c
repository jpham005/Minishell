#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*ret;
	size_t	i;

	ret = ft_malloc(sizeof(char), (ft_strlen(str) + 1) | 1);
	i = 0;
	while (str[i])
	{
		ret[i] = str[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
